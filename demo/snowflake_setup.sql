  CLAIM_NUMBER STRING,
  ROUTE STRING,
  PRIORITY STRING,
  DECISION STRING,
  PAYLOAD VARIANT,
  CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

