  POLICY_NUMBER STRING,
  POLICY_STATUS STRING,
  POLICY_AGE_MONTHS NUMBER,
  COVERAGE_TIER STRING,
  DEDUCTIBLE NUMBER,
  COVERAGE_VALID BOOLEAN,
  UPDATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

CREATE OR REPLACE TABLE CUSTOMER_CLAIM_HISTORY (
  CUSTOMER_ID NUMBER,
  PRIOR_CLAIM_COUNT NUMBER,
  PRIOR_CLAIM_TOTAL NUMBER(12, 2),
  FRAUD_FLAG BOOLEAN,
  LAST_CLAIM_AT TIMESTAMP_NTZ,
  UPDATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

CREATE OR REPLACE TABLE CLAIM_ESCALATION_AUDIT (
  EVENT_ID STRING DEFAULT UUID_STRING(),
